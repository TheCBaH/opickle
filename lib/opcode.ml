let build = 'b' (* proto 0 *)
let inst = 'i' (* proto 0 *)
let obj = 'o' (* proto 1 *)
let newobj = '\x81' (* proto 2 *)
let newobj_ex = '\x92' (* proto 4 *)

(* Framing / protocol *)
